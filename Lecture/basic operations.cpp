#include<iostream>
using namespace std;
// the basic operations
// the arithmetic operations
int  main(){
	int a=4;
	int b=5;
	cout<<"the sum of a and b:"<<a+b<<endl;
	cout<<"the dif of a and b:"<<a-b<<endl;
	cout<<"the mult of a and b:"<<a*b<<endl;
	cout<<"the div of a and b:"<<a/b<<endl;
	cout<<"the remainder of a and b:"<<a%b<<endl;
	cout<<"the value of the a++:"<<a++<<endl;
	cout<<"the value of the a--:"<<a--<<endl;
	cout<<"the value of the ++a:"<<++a<<endl;
	cout<<"the value of the --a:"<<--a<<endl;
//the camparson Operator
    int x=5,y=6;
    cout<<"the value of x==y:"<<(x==y)<<endl;
    cout<<"the value of x!=y:"<<(x!=y)<<endl;
    cout<<"the value of x<=y:"<<(x<=y)<<endl;
    cout<<"the value of x>=y:"<<(x>=y)<<endl;
// the logical operator
    cout<<"the value of ((x==y)&&(x<=y)):"<<((x==y)&&(x<=y))<<endl;
    cout<<"the value of ((x==y)||(x<=y)):"<<((x==y)||(x<=y))<<endl;
    cout<<"the value of !(x==y):"<<!(x==y)<<endl;
    
   
	return 0;
}
